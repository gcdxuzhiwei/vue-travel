<template>
    <div>
        <detail-banner :sightName="this.sightName" :bannerImg="bannerImg" :bannerImgs="gallaryImgs"></detail-banner>
        <detail-header></detail-header>
        <detail-list :list="list"></detail-list>
        <div class="content"></div>
    </div>
</template>

<script>
    import {ref,onMounted} from 'vue'
    import {useRoute} from 'vue-router'
    import DetailBanner from "../components/Detail/Banner";
    import DetailHeader from "../components/Detail/Header";
    import DetailList from "../components/Detail/List";
    import axios from 'axios'
    export default {
        name: "Detail",
        components: {DetailList, DetailHeader, DetailBanner},
        setup(){
            const sightName=ref('')
            const bannerImg=ref('')
            const gallaryImgs=ref([])
            const list=ref([])
            const route=useRoute()
            async function getDetailInfo(){
                let res=await axios.get('/api/detail.json',{
                    params:{
                        id:route.params.id
                    }
                })
                res=res.data
                if(res.ret&&res.data){
                    const data=res.data
                    sightName.value=data.sightName
                    bannerImg.value=data.bannerImg
                    gallaryImgs.value=data.gallaryImgs
                    list.value=data.categoryList
                }
            }
            onMounted(()=>{
                getDetailInfo()
            })
            return {sightName,bannerImg,gallaryImgs,list}
        }
    }
</script>

